import Foundation

class Leetcode435 {
    func eraseOverlapIntervals(_ intervals: [[Int]]) -> Int {
        let intervals = intervals.sorted { $0[1] < $1[1] }
        var right = intervals[0][1]
        var ans = 1
        for index in 1..<intervals.count {
            if right <= intervals[index][0] {
                ans += 1
                right = intervals[index][1]
            }
        }
        return intervals.count - ans
    }
    
